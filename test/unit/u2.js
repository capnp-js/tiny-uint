/* @flow */

import test from "ava";

import { u2_mask } from "../../src/u2";

test("`u2_mask` with value 0", t => {
  t.plan(4);

  t.is(u2_mask(0x00, 0x00), 0x00);
  t.is(u2_mask(0x00, 0x01), 0x00);
  t.is(u2_mask(0x00, 0x02), 0x00);
  t.is(u2_mask(0x00, 0x03), 0x00);
});

test("`u2_mask` with value 1", t => {
  t.plan(4);

  t.is(u2_mask(0x01, 0x00), 0x00);
  t.is(u2_mask(0x01, 0x01), 0x01);
  t.is(u2_mask(0x01, 0x02), 0x00);
  t.is(u2_mask(0x01, 0x03), 0x01);
});

test("`u2_mask` with value 2", t => {
  t.plan(4);

  t.is(u2_mask(0x02, 0x00), 0x00);
  t.is(u2_mask(0x02, 0x01), 0x00);
  t.is(u2_mask(0x02, 0x02), 0x02);
  t.is(u2_mask(0x02, 0x03), 0x02);
});

test("`u2_mask` with value 3", t => {
  t.plan(4);

  t.is(u2_mask(0x03, 0x00), 0x00);
  t.is(u2_mask(0x03, 0x01), 0x01);
  t.is(u2_mask(0x03, 0x02), 0x02);
  t.is(u2_mask(0x03, 0x03), 0x03);
});
